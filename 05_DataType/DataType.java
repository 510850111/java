class DataType{
	public static void main(String[] args){
		//定义一个byte变量
		byte b = 10;
		System.out.println(b);

		//定义一个short变量
		short s = 100;
		System.out.println(s);

		//定义一个Int 变量
		int i = 1000;
		System.out.println(i);
		
		//定义一个long变量
		long l = 10000L;
		System.out.println(l);
		
		//定义一个char型变量
		char c = 'a';	
		System.out.println(c);

		
		//定义一个float变量
		float f = 123.456F;
		System.out.println(f);	

		//定义一个double变量
	
		double d = 123.465789;
		System.out.println(d);

		//定义一个布尔型变量
		boolean boo = true;
		System.out.println(boo);
		
		
		
	}
}
